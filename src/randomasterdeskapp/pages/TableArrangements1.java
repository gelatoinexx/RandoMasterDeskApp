    private void jButtonReshuffleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReshuffleActionPerformed
    
        // Store the original column names and their indices
        List<Object> originalColumnNames = new ArrayList<>();
        List<Integer> originalColumnIndices = new ArrayList<>();
        for (int i = 0; i < tableModel.getColumnCount(); i++) {
            originalColumnNames.add(tableModel.getColumnName(i));
            originalColumnIndices.add(i);
        }

        // Generate the groupings of scholars using the tableSize variable
        List<List<String>> groupings = Randomizer.generateGroupings(tableSize);

        // Clear the existing table data
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            for (int j = 0; j < tableModel.getColumnCount(); j++) {
                tableModel.setValueAt("", i, j);
            }
        }

        // Update the existing columns with the new data
        for (int i = 0; i < groupings.size(); i++) {
            List<String> table = groupings.get(i);
            if (i < originalColumnIndices.size()) {
                int columnIndex = originalColumnIndices.get(i);
                for (int j = 0; j < table.size(); j++) {
                    String value = table.get(j);
                    tableModel.setValueAt(value, j, columnIndex);
                }
            } else {
                tableModel.addColumn("Table " + (i + 1), table.toArray());
            }
        }

    }//GEN-LAST:event_jButtonReshuffleActionPerformed

